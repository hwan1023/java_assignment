package com.hanhwa_tae.level01.basic;

public class assignment1 {
    public static void main(String[] args) {
        int firstNum = 20;
        int secondNum = 30;

        System.out.println("더하기 결과 : " + (firstNum + secondNum));
        System.out.println("빼기 결과 : " + (firstNum - secondNum));
        System.out.println("곱하기 결과 : " + (firstNum * secondNum));
        System.out.println("나누기한 몫 : " + (firstNum / secondNum));
        System.out.println("나누기한 나머지 : " + (firstNum % secondNum));
    }
}


